package com.mossy.shared.cash.enums;

public enum SellerEventType {
    /* ----------------------------------------------------------------
     * 정산(Payout) 공통 이벤트
     * - 기존 PayoutEventType 값을 이 enum으로 통합
     * ---------------------------------------------------------------- */
    정산__상품판매_대금,
    정산__상품판매_수수료,
    정산__상품판매_기부금,
    정산__상품환불_대금,
    정산__상품환불_수수료,
    정산__상품환불_기부금,
    정산__프로모션_플랫폼부담,
    정산__상품환불_플랫폼부담,

    /* ----------------------------------------------------------------
     * 판매자(Seller) 액션 관련 이벤트
     * ---------------------------------------------------------------- */
    정산수령__일괄지급,       // 정산 배치에서 항목 합산 후 1회 입금할 때
    정산수령__상품판매_대금,   // 정산 완료 후 판매자 지갑에 돈이 들어올 때
    차감__상품판매_수수료,    // (필요 시) 정산 금액에서 수수료를 떼고 기록할 때
    출금__계좌이체,           // 판매자가 자신의 수익을 인출할 때

    /* ----------------------------------------------------------------
     * 관리자/시스템(System) 전용 관리 이벤트
     * ---------------------------------------------------------------- */
    임시보관__주문결제,       // 구매자가 낸 돈을 시스템이 보관할 때 (홀딩)
    보관해제__정산확정,       // 구매 확정 등으로 보관하던 돈을 판매자에게 줄 때
    보관해제__주문취소,       // 주문 취소로 보관하던 돈을 구매자에게 환불할 때
    수익__판매수수료,          // 플랫폼의 순수익(수수료)이 확정될 때
    적립__상품판매_기부금     // 상품 판매로 발생한 기부금이 확정될 때 (기부처 계정 입금)
}
